# [요약] NSFNET 토폴로지 정의 — 미국 백본망 14 노드 / 21 링크
# - EDGES 목록 → ADJACENCY dict 생성, 토폴로지 일반화 검증 실험에 사용
# =============================================================================
NUM_NODES = 14

# NSFNET 14노드 토폴로지 (21개 링크)
# 노드: 0=시애틀, 1=캘리포니아(CA1), 2=캘리포니아(CA2), 3=유타, 4=콜로라도,
#       5=텍사스, 6=네브래스카, 7=일리노이, 8=피츠버그, 9=조지아,
#       10=미시간, 11=뉴욕, 12=뉴저지, 13=메릴랜드
EDGES = [
    (0, 1), (0, 2), (0, 7),
    (1, 2), (1, 3),
    (2, 5),
    (3, 4), (3, 6),
    (4, 5), (4, 11),
    (5, 6), (5, 9), (5, 13),
    (6, 7),
    (7, 8), (7, 10),
    (8, 9), (8, 11), (8, 13),
    (9, 12),
    (10, 11),
]


def build_adjacency():
    adj = {i: [] for i in range(NUM_NODES)}
    for u, v in EDGES:
        adj[u].append(v)
        adj[v].append(u)
    return adj


ADJACENCY = build_adjacency()
