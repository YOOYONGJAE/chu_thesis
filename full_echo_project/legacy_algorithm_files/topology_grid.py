# =============================================================================
# [요약] 6x6 grid 토폴로지 정의 — 36 노드, EDGES 목록 → ADJACENCY dict 생성
# - 가로 링크 일부가 제거된 변형 grid >> 병목 (bridge) 링크가 존재해
#   link_cut 실험 ((14,15) top / (2,3) bottom) 의 대상이 됨
# =============================================================================
NUM_NODES = 36

EDGES = [
    # 맨 윗줄
    (0, 1), (1, 2), (2, 3), (3, 4), (4, 5),
    # 맨 윗줄 -> 둘째 줄
    (0, 6), (5, 11),
    # 둘째 줄 가로
    (7, 8), (9, 10),
    # 둘째 줄 -> 셋째 줄
    (6, 12), (7, 13), (8, 14), (9, 15), (10, 16), (11, 17),
    # 셋째 줄 가로
    (12, 13), (13, 14), (14, 15), (15, 16), (16, 17),
    # 셋째 줄 -> 넷째 줄
    (12, 18), (13, 19), (14, 20), (15, 21), (16, 22), (17, 23),
    # 넷째 줄 가로
    (18, 19), (19, 20), (21, 22), (22, 23),
    # 넷째 줄 -> 다섯째 줄
    (18, 24), (19, 25), (20, 26), (21, 27), (22, 28), (23, 29),
    # 다섯째 줄 가로
    (24, 25), (25, 26), (27, 28), (28, 29),
    # 다섯째 줄 -> 맨 아랫줄
    (24, 30), (25, 31), (26, 32), (27, 33), (28, 34), (29, 35),
    # 맨 아랫줄 가로
    (30, 31), (31, 32), (33, 34), (34, 35),
]


def build_adjacency():
    adj = {i: [] for i in range(NUM_NODES)}
    for u, v in EDGES:
        adj[u].append(v)
        adj[v].append(u)
    return adj


ADJACENCY = build_adjacency()
