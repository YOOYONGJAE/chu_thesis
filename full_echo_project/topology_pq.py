# [요약] PQ-routing 15노드 토폴로지 정의 — 15 노드 / 21 링크
# - 출처: Choi & Yeung, "Predictive Q-Routing: A Memory-based Reinforcement
#   Learning Approach to Adaptive Traffic Control", NeurIPS 1995, Figure 1(a)
# - EDGES 목록 → ADJACENCY dict 생성 (grid / nsfnet 과 동일 인터페이스)
# - 노드 번호: 0-인덱스. 논문 노드 n = 코드 노드 n-1
#
# [구조 특징]
# - 논문 소스: 12, 13, 14 (코드 11, 12, 13) → 목적지: 15 (코드 14)
# - 세 소스의 홉수 최단경로가 모두 노드 4 (코드 3) 와 링크 4-15 로 수렴
#   → 노드 4 가 의도된 병목 (모든 노드는 tick 당 1 패킷 처리 가정)
# - 대안 경로 계층:
#     상단(최단): 1→4→15 / 2→4→15 / 3→4→15  (3홉)
#     중간:       1→5→6→15                    (4홉)
#     하단(우회): 2→7→8→9→10→11→15           (7홉, 논문에선 2배속 노드 구간)
#
# [주의 — 원 논문과의 차이 (현재 시뮬레이터 기준)]
# 1. 논문은 노드 7~11 (코드 6~10) 의 처리 속도를 다른 노드의 2배로 설정.
#    현재 simulator.py 는 모든 노드 tick 당 1 패킷 균일 처리 → 미반영.
#    (FAST_NODES 상수로 정의만 해 둠. 반영하려면 simulator 수정 필요)
# 2. 논문 트래픽은 고정 소스(12,13,14) → 고정 목적지(15) 패턴.
#    현재 simulator.py 는 uniform random src/dst → 미반영.
#    (SOURCES / DESTINATION 상수로 정의만 해 둠)
# → 따라서 현재 상태로는 "PQ 논문 재현" 이 아니라 "PQ 토폴로지 위에서의
#   uniform 트래픽 실험" 임. 논문 재현 실험 시 위 두 항목 반영 후
#   실험환경 장에 근거로 명시할 것.
# =============================================================================
NUM_NODES = 15

# PQ-routing 15노드 토폴로지 (21개 링크), 논문 Figure 1(a)
# 각 줄 주석 = 논문 원본 노드 번호 기준 에지
EDGES = [
    # 소스 진입 링크
    (11, 0),   # 12-1
    (12, 1),   # 13-2
    (13, 2),   # 14-3
    # 상단 경로
    (2, 3),    # 3-4
    # 노드 1, 2 의 세 갈래 분기 (지름길 4 / 중간 5 / 우회 7)
    (0, 3),    # 1-4
    (0, 4),    # 1-5
    (0, 6),    # 1-7
    (1, 3),    # 2-4
    (1, 4),    # 2-5
    (1, 6),    # 2-7
    # 허브 4 의 우측 링크
    (3, 5),    # 4-6
    (3, 14),   # 4-15 (세 최단경로가 공유하는 목적지 직결 링크)
    # 중간 경로
    (4, 5),    # 5-6
    (4, 9),    # 5-10
    (5, 14),   # 6-15
    (5, 10),   # 6-11
    # 하단 체인 (논문에선 2배속 구간)
    (6, 7),    # 7-8
    (7, 8),    # 8-9
    (8, 9),    # 9-10
    (9, 10),   # 10-11
    (10, 14),  # 11-15
]

# --- 논문 실험 설정 메타데이터 (현재 simulator 는 미사용, 향후 재현 실험용) ---
SOURCES = [11, 12, 13]          # 논문 12, 13, 14
DESTINATION = 14                # 논문 15
FAST_NODES = [6, 7, 8, 9, 10]   # 논문 7~11: 처리 속도 2배 (tick 당 2 패킷)


def build_adjacency():
    adj = {i: [] for i in range(NUM_NODES)}
    for u, v in EDGES:
        adj[u].append(v)
        adj[v].append(u)
    return adj


ADJACENCY = build_adjacency()
